/**********************************************************************************************************************/
/* Description      : Interface for hw reset cause. */
/**********************************************************************************************************************/
#pragma once

/**********************************************************************************************************************/
/* Include                                                                                                            */
/**********************************************************************************************************************/

/* Libraries */
#include <stdint.h>

/**********************************************************************************************************************/
/* Macros                                                                                                             */
/**********************************************************************************************************************/

#define HW_RESET_CAUSE_BITMAP(type) ((uint16_t)1 << type)

/**********************************************************************************************************************/
/* Typedefs                                                                                                           */
/**********************************************************************************************************************/

typedef enum {
    HW_RESET_CAUSE_NONE = 0,
    HW_RESET_CAUSE_POR,
    HW_RESET_CAUSE_BOR,
    HW_RESET_CAUSE_PIN,
    HW_RESET_CAUSE_WDG, /* WD RESET */
    HW_RESET_CAUSE_LP,  /* LOW POWER RESET */
    HW_RESET_CAUSE_FW,  /* FireWall RESET */
    HW_RESET_CAUSE_OBL, /* OptionByte Loader RESET */
} hw_reset_cause_e;

/**********************************************************************************************************************/
/* Interface Function Declarations                                                                                    */
/**********************************************************************************************************************/

/* Get the HW reset cause */
hw_reset_cause_e hw_reset_cause_get(void);

/* Clear the HW reset cause flags */
void hw_reset_cause_clear(void);